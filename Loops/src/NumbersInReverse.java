//Print numbers in reverse From 20 to 1 using while loop
public class NumbersInReverse {
    public static void main(String[] args) {

        System.out.println("numbers in reverse From 20 to 1");
        //Printing numbers in reverse using while loop.
         int num = 20;
         while (num>=1)
            {
             System.out.println(num);
             num--;
            }
    }
}
