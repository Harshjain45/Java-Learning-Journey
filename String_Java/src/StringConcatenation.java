/* String Concatenation:Concatenate two strings: "Hello" and "World"
   using + operator and print the result. */
public class StringConcatenation {
    public static void main(String[] args) {
        //String "Hello".
        String Hello = "Hello";

        //String "World".
        String World = "World";

        //Concatenating Using [+] operator.
        System.out.println(Hello + " " + World);

        // Another Method: concat method.
       //  System.out.println(Hello.concat(World));
    }
}
