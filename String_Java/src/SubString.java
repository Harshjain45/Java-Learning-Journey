// Substring: Declare String str = "Developer" and print substring from index 3 to 7.
public class SubString {
    public static void main(String[] args) {
        //Declaring String str.
        String str = "Developer";
        System.out.println("String str : " + str);

        //Creating a substring from index 3 to 7.
        String substring = str.substring(3,8);

        //Printing a substring from index 3 to 4.
        System.out.println("str substring from index 3 to 7 : " + substring);
    }
}
