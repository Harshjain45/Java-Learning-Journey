/* Write a Java program to add three numbers. */
public class AddingThreeNumbers {
    public static void main(String[] args) {
        // Number 1
        System.out.println("Number 1 = 10");
        int Number1 = 10;

        // Number 2
        System.out.println("Number 2 = 20");
        int Number2 = 20;

        // Number 3
        System.out.println("number 3 = 30");
        int Number3 = 30;

        // sum
        int Sum = Number1 + Number2+ Number3;

        // Printing Sum.
        System.out.println("Sum of three numbers : "+Sum);
    }
}
